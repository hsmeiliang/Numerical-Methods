#include<stdio.h>
#include<math.h>



int main()
{
    double A[10][10]={0},D[10][10]={0},LU[10][10]={0};
    double x[10]={0},x1[10]={0},b[10]={2,1,1,1,1,1,1,1,1,2};
    int n=10;
               A[0][0]=3;A[0][1]=-1;
    A[1][0]=-1;A[1][1]=3;A[1][2]=-1;
    A[2][1]=-1;A[2][2]=3;A[2][3]=-1;
    A[3][2]=-1;A[3][3]=3;A[3][4]=-1;
    A[4][3]=-1;A[4][4]=3;A[4][5]=-1;
    A[5][4]=-1;A[5][5]=3;A[5][6]=-1;
    A[6][5]=-1;A[6][6]=3;A[6][7]=-1;
    A[7][6]=-1;A[7][7]=3;A[7][8]=-1;
    A[8][7]=-1;A[8][8]=3;A[8][9]=-1;
    A[9][8]=-1;A[9][9]=3;

    for(int i=0;i<n;i++)
    {
            if(i<9) LU[i][i+1]=A[i][i+1];
            if(i>0) LU[i][i-1]=A[i][i-1];
    }
    for(int i=0;i<n;i++)
    {
        D[i][i]=1/A[i][i];
    }
    int k=0;
    while(k<=10)
    {
        for(int i=0;i<n;i++)
        {
            x1[i]=0;
            for(int j=0;j<n;j++)
            {
                x1[i]=x1[i]+LU[i][j]*x[j];
            }
        }
        for(int i=0;i<n;i++)
        {
            x1[i]=b[i]-x1[i];
        }
        for(int i=0;i<n;i++)
        {
            x[i]=0;
            for(int j=0;j<n;j++)
            {
                x[i]=x[i]+D[i][j]*x1[j];
            }

        }
        k++;
    }
    for(int i=0;i<n;i++)
    {
        printf("x_%d = %.3f\n",i+1,x[i]);
    }
}
